#1. Construa um programa no qual um usuário informe a sua estatura em metros e o programa converta-a para centímetros
user_m_height = float(input("Insira sua altura em metros: "))
user_cm_height = user_m_height*100
print(f"Sua altura em centímetros é {user_cm_height}.")
